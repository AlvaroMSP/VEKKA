    private void BGeneralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BGeneralActionPerformed
        String folio = CBFolio.getSelectedItem().toString();
        Date fi = DCInicial.getDate();
        Date ff = DCFinal.getDate();
        String fini = cnx.toSQL(fi);
        String ffin = cnx.toSQL(ff);

        String ventas = "SELECT folio, SUM(cantidadp*preciod) AS Total, SUM(cantidadp*(preciod-preciobase)) AS Ganancias "
                + "FROM detalles, pedidos, productos "
                + "WHERE folio = pedidos_folio AND idproductos=productos_idproductos AND "
                + "fecha BETWEEN '" + fini + "' AND '" + ffin + "' "
                + "GROUP BY folio";

        cnx.entablar(ventas, TQuery);

        series.clear();
        series.add("Ventas");
        series.add("Ganancias");
        datos = cnx.consultar(ventas);

        PGrafica.removeAll();
        chart = new GraficaXY("Ventas", "Pedidos", "Total", series, datos);
        PGrafica.add(chart.chartPanel);
        PGrafica.updateUI();
    }//GEN-LAST:event_BGeneralActionPerformed

