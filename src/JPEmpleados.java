    private void BBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BBorrarActionPerformed
        int rensel = TEmpleados.getSelectedRow();
        if (rensel > -1) {
            DefaultTableModel datos = (DefaultTableModel) TEmpleados.getModel();
            String codigo = datos.getValueAt(rensel, 0).toString();

            String id = TFCodigo.getText();
            String nom = TFNom.getText();
            String tel = TFTel.getText();
            String pue = TFPuesto.getText();

            Empleados cl = new Empleados(id, nom, tel, pue);
            if (cl.remover(cnx) == 1) {
                String sql = "SELECT * FROM empleados";
                JOptionPane.showMessageDialog(this, "Empleado Borrado");
                cnx.entablar(sql, TEmpleados);

                TFCodigo.setText("");
                TFNom.setText("");
                TFTel.setText("");
                TFPuesto.setText("");

            } else {
                JOptionPane.showMessageDialog(this, "Error al borrar");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Primero selecciona al empleado de la Tabla");

        }
    }//GEN-LAST:event_BBorrarActionPerformed

