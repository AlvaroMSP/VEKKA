    private void BRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BRegistrarActionPerformed
        String id = TFCodigo.getText();
        String nom = TFNom.getText();
        String tel = TFTel.getText();
        String pue = TFPuesto.getText();

        Empleados em = new Empleados(id, nom, tel, pue);

        if (id.equals("")) {
            JOptionPane.showMessageDialog(this, "Hubo un error, intente de nuevo");
        } else if (em.insertar(cnx) == 1) {
            JOptionPane.showMessageDialog(this, "Empleado agregado");
            cnx.entablar("SELECT * FROM empleados", TEmpleados);

            TFCodigo.setText("");
            TFNom.setText("");
            TFTel.setText("");
            TFPuesto.setText("");

        }
    }//GEN-LAST:event_BRegistrarActionPerformed

    private void TEmpleadosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TEmpleadosMousePressed
        int ren = TEmpleados.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) TEmpleados.getModel();

        if (ren >= 0) {
            TFCodigo.setText(modelo.getValueAt(ren, 0).toString());
            TFNom.setText(modelo.getValueAt(ren, 1).toString());
            TFTel.setText(modelo.getValueAt(ren, 2).toString());
            TFPuesto.setText(modelo.getValueAt(ren, 3).toString());

        }
    }//GEN-LAST:event_TEmpleadosMousePressed

    private void BActuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BActuActionPerformed
        String id = TFCodigo.getText();
        String nom = TFNom.getText();
        String tel = TFTel.getText();
        String pue = TFPuesto.getText();

        Empleados cl = new Empleados(id, nom, tel, pue);
        if (cl.actualizar(cnx) == 1) {
            String sql = "SELECT * FROM empleados";
            JOptionPane.showMessageDialog(this, "Datos Actualizados");
            cnx.entablar(sql, TEmpleados);

            TFCodigo.setText("");
            TFNom.setText("");
            TFTel.setText("");
            TFPuesto.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar");
        }
    }//GEN-LAST:event_BActuActionPerformed

